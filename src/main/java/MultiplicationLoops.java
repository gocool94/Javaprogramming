public class MultiplicationLoops {

    public static void main(String[] args){


        int n = 9;
        // 2 x 1 = 2

        System.out.println(n +" Tables");
        for (int i = 1 ;i<=10;i++){
            System.out.println(n +" x " + i + " = " + (n*i));
        }
    }
}



