public class Forloops {


    public static void main(String[] args){

        //print and segregate even and odd numbers

        int n = 100;

        for (int i =0;i<n;i++){

            if(i%2 == 0 ){
                System.out.println(i+" is a even number");
            } else {
                System.out.println(i+" is a odd number");
            }

        }
    }
}
